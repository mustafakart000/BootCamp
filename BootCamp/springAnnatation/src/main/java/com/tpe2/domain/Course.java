package com.tpe2.domain;


import org.springframework.stereotype.Component;
@Component

public class Course {


    private String name;

    private int id;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
